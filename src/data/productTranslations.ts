const getRawProductTextFields = (product: RuntimeListingProduct): ProductTextFields => ({
    season: product.season,
    gender: product.gender,
    productName: product.productName,
    colorRange: product.colorRange,
    genre: product.genre,
    sizeRange: product.sizeRange,
    material: product.material,
    feature: product.feature,
    trimSpec: product.trimSpec,
});

export const getLocalizedProductTextFields = (
    product: RuntimeListingProduct,
    language: AppLanguage,
): ProductTextFields => {
    if (language === 'ja') {
        return getRawProductTextFields(product);
    }

    const translated = PRODUCT_TRANSLATIONS_BY_ID[product.id]?.[language as ProductTranslationLanguage];
    if (!translated) {
        return getRawProductTextFields(product);
    }

    return translated;
};
